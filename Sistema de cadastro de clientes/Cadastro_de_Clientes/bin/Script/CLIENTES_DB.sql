CREATE DATABASE CLIENTES_DB;
USE CLIENTES_DB;

CREATE TABLE TB_CLIENTES(
	ID_CLIENTE int AUTO_INCREMENT NOT NULL,
	CLIENTE_NOME varchar(256) NOT NULL,
	CLIENTE_IDADE varchar(256) NOT NULL,
	CLIENTE_EMAIL varchar(256) NOT NULL,
	CLIENTE_CPF_CNPJ varchar(256) NOT NULL,
	CLIENTE_CONTATO varchar(256) NOT NULL,
	CLIENTE_SEXO varchar(2) NOT NULL,
	CLIENTE_CADASTRO datetime NOT NULL,
	CLIENTE_TIPO varchar(256) NOT NULL,
	CLIENTE_ATIVO varchar(256) NOT NULL,
	CLIENTE_CIDADE varchar(256) NOT NULL,
	CLIENTE_BAIRRO varchar(256) NOT NULL,
	CLIENTE_RUA varchar(256) NOT NULL,
	CLIENTE_NUMERO varchar(256) NOT NULL,
	CLIENTE_UF varchar(26) NOT NULL,
	CLIENTE_CEP varchar(256) NOT NULL,
	CLIENTE_DESCRICAO varchar(256) NOT NULL,
    CONSTRAINT PK_ID_CLIENTE PRIMARY KEY(ID_CLIENTE)
)ENGINE=InnoDB;

