CREATE DATABASE IF NOT EXISTS CLIENTES_DB;
USE CLIENTES_DB;

CREATE TABLE IF NOT EXISTS TB_CLIENTES(
	ID_CLIENTE INT AUTO_INCREMENT NOT NULL,
    CLIENTE_NOME VARCHAR(256),
    CLIENTE_IDADE VARCHAR(256),
    CLIENTE_EMAIL VARCHAR(256),
    CLIENTE_CPF_CNPJ VARCHAR(256),
    CLIENTE_CONTATO VARCHAR(256),
    CLINETE_SEXO VARCHAR(2),
    CLINETE_CADASTRO DATETIME,
    CLINETE_TIPO VARCHAR(256),
    CLINETE_ATIVO VARCHAR(256), 
    CLINETE_CIDADE VARCHAR(256),
    CLINETE_BAIRRO VARCHAR(256),
    CLINETE_RUA VARCHAR(256),
    CLINETE_NUMERO VARCHAR(256)	,
    CLINETE_UF VARCHAR(256),
    CLINETE_CEP VARCHAR(256),
    CLINETE_DESCRICAO VARCHAR(256),
	CONSTRAINT PK_ID_CLIENTE PRIMARY KEY(ID_CLIENTE)
)ENGINE=InnoDB;


SELECT * FROM TB_CLIENTES ;


